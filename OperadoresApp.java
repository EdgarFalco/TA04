
public class OperadoresApp {

	public static void main(String[] args) {
		
		int x = 2;
		int y = 10;
		double n = 5;
		double m = 5.25;
		
		System.out.println("El valor de cada variable:");
		System.out.println(x + " " + y + " " + n + " " + m );
		System.out.println("La suma X + Y");
		System.out.println(x + y);
		System.out.println("La diferencia X - Y");
		System.out.println(x - y);
		System.out.println("El producto X * Y");
		System.out.println(x * y);
		System.out.println("El cociente X / Y");
		System.out.println(x / y);
		System.out.println("El resto X % Y");
		System.out.println(x % y);
		System.out.println("La suma N + M");
		System.out.println(n + m);
		System.out.println("La diferencia N - M");
		System.out.println(n - m);
		System.out.println("El producto N * M");
		System.out.println(n * m);
		System.out.println("El cociente N / M");
		System.out.println(n / m);
		System.out.println("El resto N % M");
		System.out.println(n % m);
		System.out.println("La suma X + N");
		System.out.println(x + n);
		System.out.println("El cociente Y / M");
		System.out.println(y / m);
		System.out.println("El resto Y % M");
		System.out.println(y % m);
		System.out.println("El doble de cada variable");
		System.out.println(x * 2 + " " + y * 2 + " " + n * 2 + " " + m * 2);
		System.out.println("La suma de todas la variables");
		System.out.println(x + y + n + m);
		System.out.println("El producto de todas la variables");
		System.out.println(x * y * n * m);
	}

}
