
public class VariablesNumericasApp {

	public static void main(String[] args) {
		
		int num1 = 10;
		int num2 = 5;
		int result;
		
		result = num1 + num2;
		System.out.println(num1 + " + " + num2 + " = " + result);
		result = num1 - num2;
		System.out.println(num1 + " - " + num2 + " = " + result);
		result = num1 * num2;
		System.out.println(num1 + " * " + num2 + " = " + result);
		result = num1 / num2;
		System.out.println(num1 + " / " + num2 + " = " + result);
		result = num1 % num2;
		System.out.println(num1 + " mod " + num2 + " = " + result);
	}

}
